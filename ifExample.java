class ifExample {
  public static void main (String args[]){
    int x,y; //two variables of int data type
    x = 10000;
    y = 200;
    if (x<y)
    {System.out.print("x is less than y");}
    x = x*2;//x=20
    if (x>y)
    {System.out.print("x is greater than y");}
    if (x == y)
    {System.out.print("x is now equal to y");}
  }
}